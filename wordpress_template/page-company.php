<h3 class="title01 mbm00">MANGAについて</h3>
<p>5ch発祥の、ERC20準拠の仮想通貨「MangaCoin」を運営している会社です。仮想通貨「MangaCoin」を使い、クラウドファンディングのマンガ版、名付けて「クラウドマンギング」の実現を目指しています。</p><p>
MangaCoinは漫画家の方を支援する目的で作られた、５ｃｈ発祥のイーサリアムトークンです。2018年4月10日に誕生した、まだ新しい仮想通貨です。
クラウドファンディングのマンガ版の事を「クラウドマンギング」と名付け、それをメインプロジェクトとしています。</p><p>
このプロジェクトには、「面倒な登録不要」、「目標達成の確認が容易」など、仮想通貨の特性を活かした利点があり、さらには「特定分野におけるクラウドファンディングに焦点をあてている」という、他に類を見ないプロジェクトです。また、「MANGA」という言葉は、日本の皆様、ひいては世界中の人々に広く愛されている、親しみやすい言葉です。 <br>
このMangaCoinが漫画家・読者の間で広く普及することを、私たちは望んでいます。</p>	
